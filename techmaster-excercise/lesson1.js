function rectangleArea(a, b) {
  console.log((a + b) * 2);
}

function rectanglePerimeter(a, b) {
  console.log(a * b);
}

function circleArea(r) {
  console.log(r * r * Math.PI);
}

function circlePerimeter(r) {
  console.log(2 * r * Math.PI);
}

function calculate(a, b) {
  console.log(-b / a);
}

function cmToOther(a) {
  console.log(a * 10, " mm");
  console.log(a / 100, " m");
  console.log(a / 100000, " km");
}

function celciusToOther(a) {
  console.log(a * 1.8 + 32, " F");
  console.log(a + 273.15, " K");
}
